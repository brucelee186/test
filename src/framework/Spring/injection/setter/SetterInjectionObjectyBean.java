package framework.Spring.injection.setter;

public class SetterInjectionObjectyBean {
	private int year;
	private SetterInjectionPropertyBean setterInjectionPropertyBean;
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public SetterInjectionPropertyBean getSetterInjectionPropertyBean() {
		return setterInjectionPropertyBean;
	}
	public void setSetterInjectionPropertyBean(
			SetterInjectionPropertyBean setterInjectionPropertyBean) {
		this.setterInjectionPropertyBean = setterInjectionPropertyBean;
	}
}
