package framework.Spring.injection.constructon;

public class ConstructonInjectionObjectBean {
	private ConstructonInjectionPropertyBean constructonInjectionPropertyBean;
	private String type;
	public ConstructonInjectionObjectBean(
			ConstructonInjectionPropertyBean constructonInjectionPropertyBean,
			String type) {
		super();
		this.constructonInjectionPropertyBean = constructonInjectionPropertyBean;
		this.type = type;
	}
	public ConstructonInjectionPropertyBean getConstructonInjectionPropertyBean() {
		return constructonInjectionPropertyBean;
	}
	public void setConstructonInjectionPropertyBean(
			ConstructonInjectionPropertyBean constructonInjectionPropertyBean) {
		this.constructonInjectionPropertyBean = constructonInjectionPropertyBean;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
